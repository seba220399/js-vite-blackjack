/**
 *
 * @param {String} carta carta retornada de funcion pedirCarta()
 * @returns retorna valor numeral de la carta
 */

export const valorCarta = ( carta ) => {
  const valor = carta.substring(0,carta.length-1);
  return  (isNaN( valor))?
          (valor === 'A')? 11 : 10
          : valor*1;
}

export default valorCarta
